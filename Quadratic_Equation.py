#Formula for Quadratic Equation
b=int(input("Enter the value of b"))
a=int(input("Enter the value of a"))
c=int(input("Enter the value of c"))
Positive_Root=(((-b)+((b*b-4*a*c)*0.2))/2*a)
Negative_Root=(((-b)-((b*b-4*a*c)*0.2))/2*a)
print("Positive Roots of Quadractic Equation :",Positive_Root)
print(" Negative Roots of Quadractic Equation :",Negative_Root)
